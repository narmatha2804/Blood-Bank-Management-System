    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String BloodGroup = (String)jComboBox1.getSelectedItem();
        String Available = jTextField1.getText();
        int available = Integer.parseInt(Available);
        try{
             Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("update Stock set Available=available+"+available+" where BloodGroup='"+BloodGroup+"'" );
            
            JOptionPane.showMessageDialog(null, "Successfully updated");
            setVisible(false);
            new stockIncrease().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

